let numero = parseFloat(prompt("insira um numero"))

if (isNaN(numero)){
    alert("você não inseriu um número")
}
if (numero > 0){
    alert("o número é positivo")
}
if (numero ==0){
    alert("O número é neutro")
}
if (numero < 0) {
    alert("O número não é positivo")
}
